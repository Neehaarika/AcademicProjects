    private void populateCombo() {
        organizationJComboBox.removeAllItems();
        
        switch (enterprise.getEnterpriseType()) {
            case Crime:
                for (Organization.CrimeType c : Organization.CrimeType.values()) {
                    organizationJComboBox.addItem(c);
                }               
                break;
            case Health:
                for (Organization.HealthType c : Organization.HealthType.values()) {
                    organizationJComboBox.addItem(c);
                }
                break;
            case Education:
                for (Organization.EducationType c : Organization.EducationType.values()) {
                    organizationJComboBox.addItem(c);
                }
                break;
            default:
                break;
        }
        

    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        for (Organization organization : directory.getOrganizationList()) {
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();

            model.addRow(row);
        }
    }

