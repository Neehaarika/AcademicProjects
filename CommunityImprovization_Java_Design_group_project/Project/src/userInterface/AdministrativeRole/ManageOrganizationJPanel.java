    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        switch (enterprise.getEnterpriseType()) {
            case Crime:
                CrimeType type = (CrimeType) organizationJComboBox.getSelectedItem();
                directory.createCrimeOrganization(type);
                break;
            case Health:
                HealthType type1 = (HealthType) organizationJComboBox.getSelectedItem();
                directory.createHealthOrganization(type1);
                break;
            case Education:
                EducationType type2 = (EducationType) organizationJComboBox.getSelectedItem();
                directory.createEducationOrganization(type2);
                break;
            default:
                break;
        }

        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

