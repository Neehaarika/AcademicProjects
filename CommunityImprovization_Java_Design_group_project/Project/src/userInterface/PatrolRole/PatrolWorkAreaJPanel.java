    public void PopulateRequestTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblRequest.getModel();
        dtm.setRowCount(0);

        for (WorkRequest workRequest : patrolOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[6];
            row[0] = ((PoliceToPatrolWorkRequest) workRequest).getArea();
            row[1] = workRequest;
            row[2] = workRequest.getStatus();
            row[3] = ((PoliceToPatrolWorkRequest) workRequest).getRequestStatus();
            row[4] = workRequest.getSender();
            row[5] = workRequest.getReceiver() == null ? null : workRequest.getReceiver().getEmployee().getName();
            dtm.addRow(row);
        }
    }

