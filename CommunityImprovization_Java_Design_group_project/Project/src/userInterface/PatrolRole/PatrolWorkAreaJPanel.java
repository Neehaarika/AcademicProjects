    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRequest.getSelectedRow();

        if (selectedRow >= 0) {
            PoliceToPatrolWorkRequest request = (PoliceToPatrolWorkRequest) tblRequest.getValueAt(selectedRow, 1);

            if (request.getReceiver() == null) {
                request.setStatus("Processing");
                request.setReceiver(account);
                PopulateRequestTable();
            } else {
                JOptionPane.showMessageDialog(this, "This Complaint is already Processed", "Sorry", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "PLEASE SELECT A ROW TO VIEW DETAILS", "NO INPUT DETECTED!!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAssignActionPerformed

    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRequest.getSelectedRow();

        if (selectedRow >= 0) {
            PoliceToPatrolWorkRequest request = (PoliceToPatrolWorkRequest) tblRequest.getValueAt(selectedRow, 1);
            if (request.getStatus() == "Processing" && request.getReceiver() != null) {
                request.setRequestStatus("Approved");
                request.setStatus("Approved");
                PopulateRequestTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "PLEASE SELECT A ROW TO VIEW DETAILS", "NO INPUT DETECTED!!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnApproveActionPerformed


